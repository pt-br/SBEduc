    private void bConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmaActionPerformed
       if(!tNumReg.getText().equals("") && !tAutor.getText().equals("") && !tTitulo.getText().equals("") && !tEditora.getText().equals("") && !tQuantidade.getText().equals("") && !tCategoria.getSelectedItem().toString().equals("-Selecione-") ){
           MySQL sql = new MySQL();
           sql.regLivro(tNumReg.getText().toUpperCase(),tTitulo.getText().toUpperCase(),tAutor.getText().toUpperCase(),data,tEditora.getText().toUpperCase(),tCategoria.getSelectedItem().toString().toUpperCase(), tQuantidade.getText() );
           tNumReg.setText("");
           tAutor.setText("");
           tNumReg.setText("");
           tTitulo.setText("");
           tEditora.setText("");
           tQuantidade.setText("");
           tCategoria.setSelectedIndex(0);
       //    this.setAlwaysOnTop(false);
           JOptionPane.showMessageDialog(null, "Item do acervo cadastrado com sucesso!","Adicionar ao Acervo",JOptionPane.INFORMATION_MESSAGE);
        //   this.setAlwaysOnTop(true);
       }
       else{
      //     this.setAlwaysOnTop(false);
           JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos","Adicionar ao Acervo",JOptionPane.ERROR_MESSAGE);
     //      this.setAlwaysOnTop(true);
       }
        
        tNumReg.requestFocus();
        
    }//GEN-LAST:event_bConfirmaActionPerformed

